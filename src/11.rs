// Rust code to calculate the sum of two numbers
fn sum(a: i32, b: i32) -> i32 {
    a + b
}

// Rust code to calculate the difference between two numbers
fn diff(a: i32, b: i32) -> i32 {
    a - b
}

// Rust code to calculate the product of two numbers
fn prod(a: i32, b: i32) -> i32 {
    a * b
}

// Rust code to calculate the quotient of two numbers
fn quot(a: i32, b: i32) -> i32 {
    a / b
}

// Rust code to calculate the remainder of two numbers
fn rem(a: i32, b: i32) -> i32 {
    a % b
}
